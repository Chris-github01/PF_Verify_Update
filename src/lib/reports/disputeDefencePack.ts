function page2DataSnapshot(opts: ReportOptions, snapshotTs: string): string {
  const maxRisk = Math.max(...opts.suppliers.map(s => s.riskScore));

  const rows = opts.suppliers.map(s => {
    const ratio = riskRatio(s.riskScore, maxRisk);
    const proj = s.projectedTotal ?? s.adjustedTotal;
    const varVal = s.variationExposureValue ?? 0;
    return `<tr style="border-bottom:1px solid ${C.BORDER};">
      <td style="padding:10px 11px;font-weight:700;font-size:12px;color:${C.DARK};">${s.supplierName}</td>
      <td style="padding:10px 11px;text-align:right;font-weight:700;font-size:12px;">${fmt(s.adjustedTotal)}</td>
      <td style="padding:10px 11px;text-align:right;font-size:12px;color:${proj !== s.adjustedTotal ? C.AMBER : C.GREEN};">${fmt(proj)}</td>
      <td style="padding:10px 11px;text-align:right;font-size:12px;color:${varVal > 0 ? C.RED : C.MUTED};">${fmt(varVal)}</td>
      <td style="padding:10px 11px;text-align:center;font-size:12px;">${pct(s.coveragePercent)}</td>
      <td style="padding:10px 11px;text-align:center;font-size:12px;">${s.itemsQuoted} / ${s.totalItems}</td>
      <td style="padding:10px 11px;text-align:center;font-size:12px;">${riskLabel(ratio)}</td>
      <td style="padding:10px 11px;text-align:center;font-size:12px;">${s.weightedScore !== undefined ? `${Math.round(s.weightedScore)}/100` : '—'}</td>
      <td style="padding:10px 11px;text-align:center;font-size:12px;">${s.rank}</td>
      <td style="padding:10px 11px;text-align:center;">
        ${s.underallowanceFlag
          ? `<span style="display:inline-block;padding:2px 7px;border-radius:3px;background:${C.RED_BG};color:${C.RED};font-size:9px;font-weight:700;text-transform:uppercase;">Yes</span>`
          : `<span style="display:inline-block;padding:2px 7px;border-radius:3px;background:${C.GREEN_BG};color:${C.GREEN};font-size:9px;font-weight:700;text-transform:uppercase;">No</span>`}
      </td>
    </tr>`;
  }).join('');

  return `<div class="page">
    ${packHeader(opts, snapshotTs)}
    ${sectionHeading('2', 'Data Snapshot — Tenderer Inputs & Outputs')}

    ${infoBox(`All figures below represent the immutable data captured at snapshot timestamp <strong>${snapshotTs}</strong>. No values have been altered for the purposes of this document.`, C.BLUE, C.BLUE_BG, '#1e3a8a')}

    <div style="border:1px solid ${C.BORDER};border-radius:10px;overflow:hidden;margin-bottom:16px;font-size:11px;">
      <table>
        <thead style="background:${C.DARK};">
          <tr>
            ${['Tenderer','Quoted Total','Projected Total','Variation Exp.','Coverage %','Items (Priced/Total)','Risk Level','Score','Rank','Qty Flag'].map(h =>
              `<th style="padding:9px 11px;font-size:9px;font-weight:700;color:white;text-transform:uppercase;letter-spacing:0.4px;text-align:${h==='Tenderer'?'left':'center'};">${h}</th>`
            ).join('')}
          </tr>
        </thead>
        <tbody>${rows}</tbody>
      </table>
    </div>

    <div style="font-size:12px;color:${C.MUTED};line-height:1.6;border:1px solid ${C.BORDER};border-radius:6px;padding:12px 14px;">
      <strong>Data provenance:</strong> Quoted Totals are derived from supplier-submitted documents as parsed by the VerifyTrade extraction engine.
      Projected Totals incorporate variation exposure estimates based on scope coverage gaps and behaviour patterns.
      Scores are computed by the multi-criteria decision engine using the weights stated in Section 1.
      No manual adjustments to tenderer data have been made for this record.
    </div>

    ${sharedFooter('Data Snapshot', '2', true)}
  </div>`;
}

function page3RiskIdentification(opts: ReportOptions, snapshotTs: string): string {
  const maxRisk = Math.max(...opts.suppliers.map(s => s.riskScore));
  const hasHighRisk = opts.suppliers.some(s => riskRatio(s.riskScore, maxRisk) >= 0.6);
  const hasLowCoverage = opts.suppliers.some(s => s.coveragePercent < 75);
  const hasUnderallowance = opts.suppliers.some(s => s.underallowanceFlag);

  const riskItems: Array<{ level: string; color: string; bg: string; border: string; text: string }> = [];

  if (hasHighRisk) {
    const highRiskNames = opts.suppliers.filter(s => riskRatio(s.riskScore, maxRisk) >= 0.6).map(s => s.supplierName);
    riskItems.push({
      level: 'High', color: C.RED, bg: C.RED_BG, border: C.RED_BORDER,
      text: `${highRiskNames.join(', ')} ${highRiskNames.length === 1 ? 'was' : 'were'} identified as carrying a high-risk classification at the time of tender evaluation. This risk was recorded and factored into the composite scoring prior to any award recommendation.`,
    });
  }

  if (hasLowCoverage) {
    const lowCovNames = opts.suppliers.filter(s => s.coveragePercent < 75).map(s => `${s.supplierName} (${pct(s.coveragePercent)})`);
    riskItems.push({
      level: 'Elevated', color: C.AMBER, bg: C.AMBER_BG, border: C.AMBER_BORDER,
      text: `The following tenderers submitted below the 75% scope coverage threshold: ${lowCovNames.join(', ')}. Scope gap risk was recorded and variation exposure was quantified as part of the assessment. The projected total values in Section 2 reflect this adjustment.`,
    });
  }

  if (hasUnderallowance) {
    const uaNames = opts.suppliers.filter(s => s.underallowanceFlag).map(s => s.supplierName);
    riskItems.push({
      level: 'Elevated', color: C.AMBER, bg: C.AMBER_BG, border: C.AMBER_BORDER,
      text: `Quantity under-allowance was identified for: ${uaNames.join(', ')}. Submitted quantities fell below the reference baseline, creating a risk of post-contract measurement disputes. This risk was disclosed to the recommending authority prior to any award decision.`,
    });
  }

  if (opts.commercialWarning) {
    riskItems.push({
      level: 'Commercial Warning', color: C.AMBER, bg: C.AMBER_BG, border: C.AMBER_BORDER,
      text: opts.commercialWarning,
    });
  }

  if (riskItems.length === 0) {
    riskItems.push({
      level: 'Standard', color: C.GREEN, bg: C.GREEN_BG, border: C.GREEN_BORDER,
      text: 'No material pre-award risks were identified above standard thresholds. Risk levels across the tender field were within normal commercial bounds at the time of evaluation.',
    });
  }

  return `<div class="page">
    ${packHeader(opts, snapshotTs)}
    ${sectionHeading('3', 'Risk Identification — Pre-Award Risks on Record')}

    <div style="font-size:12px;color:${C.MID};line-height:1.7;margin-bottom:16px;">
      The following risks were identified and formally recorded during the tender evaluation process.
      Each item below represents a risk that was known to the evaluation team prior to any recommendation or award decision being made.
      The existence of these records demonstrates that risk identification was systematic and documented, not reactive.
    </div>

    ${riskItems.map((r, i) => `<div style="background:${r.bg};border:2px solid ${r.border};border-radius:8px;padding:16px 20px;margin-bottom:14px;">
      <div style="display:flex;align-items:center;gap:10px;margin-bottom:8px;">
        <span style="display:inline-block;padding:3px 10px;border-radius:4px;background:${r.color};color:white;font-size:10px;font-weight:700;text-transform:uppercase;letter-spacing:0.5px;">${r.level}</span>
        <span style="font-size:11px;font-weight:700;color:${C.MUTED};text-transform:uppercase;letter-spacing:0.5px;">Risk Item ${i + 1} of ${riskItems.length}</span>
      </div>
      <p style="font-size:13px;color:${C.MID};line-height:1.7;margin:0;">${r.text}</p>
    </div>`).join('')}

    <div style="background:${C.LIGHT_BG};border:1px solid ${C.BORDER};border-radius:8px;padding:14px 18px;font-size:12px;color:${C.MID};line-height:1.7;margin-top:8px;">
      <strong>Risk recording confirmation:</strong> The above risks were surfaced by the VerifyTrade automated risk engine and presented to the evaluating officer prior to the award recommendation being finalised.
      Risks were not suppressed, modified, or omitted from the evaluation record.
    </div>

    ${sharedFooter('Risk Identification', '3', true)}
  </div>`;
}

function page4RecommendationJustification(opts: ReportOptions, snapshotTs: string): string {
  const top = opts.suppliers[0];
  const weights = opts.scoringWeights ?? { price: 45, compliance: 20, coverage: 25, risk: 10 };
  const maxRisk = Math.max(...opts.suppliers.map(s => s.riskScore));

  const scoreRows = opts.suppliers.map(s => {
    const ratio = riskRatio(s.riskScore, maxRisk);
    return `<tr style="border-bottom:1px solid ${C.BORDER};${s.rank === 1 ? `background:${C.GREEN_BG};` : ''}">
      <td style="padding:10px 12px;font-weight:700;font-size:12px;color:${C.DARK};">${s.supplierName}</td>
      <td style="padding:10px 12px;text-align:right;font-size:12px;font-weight:600;">${fmt(s.adjustedTotal)}</td>
      <td style="padding:10px 12px;text-align:center;font-size:12px;font-weight:600;color:${s.coveragePercent >= 85 ? C.GREEN : C.AMBER};">${pct(s.coveragePercent)}</td>
      <td style="padding:10px 12px;text-align:center;font-size:12px;font-weight:600;">${riskLabel(ratio)}</td>
      <td style="padding:10px 12px;text-align:center;font-size:12px;font-weight:700;color:${C.DARK};">${s.weightedScore !== undefined ? `${Math.round(s.weightedScore)}/100` : '—'}</td>
      <td style="padding:10px 12px;text-align:center;font-size:12px;font-weight:700;color:${s.rank === 1 ? C.GREEN : C.MUTED};">
        <span style="display:inline-block;padding:3px 8px;border-radius:4px;background:${s.rank === 1 ? C.GREEN_BG : C.LIGHT_BG};color:${s.rank === 1 ? C.GREEN : C.MUTED};font-size:10px;font-weight:700;text-transform:uppercase;">${s.rank === 1 ? 'Recommended' : `Rank ${s.rank}`}</span>
      </td>
    </tr>`;
  }).join('');

  const drivers = opts.keyDecisionDrivers ?? top?.notes ?? [];
  const execText = opts.executiveSummary ?? (top
    ? `Commercial analysis of ${opts.suppliers.length} tenderer${opts.suppliers.length !== 1 ? 's' : ''} was conducted across ${top.totalItems} scope items. ${top.supplierName} was assessed as the preferred tenderer, achieving the highest composite score of ${top.weightedScore !== undefined ? `${Math.round(top.weightedScore)}/100` : 'N/A'} under the stated scoring methodology.`
    : 'Commercial analysis completed.');

  return `<div class="page">
    ${packHeader(opts, snapshotTs)}
    ${sectionHeading('4', 'Recommendation Justification')}

    <div style="background:${C.LIGHT_BG};border-left:4px solid ${C.ORANGE};border-radius:0 8px 8px 0;padding:16px 20px;margin-bottom:18px;">
      <div style="font-size:11px;font-weight:700;text-transform:uppercase;letter-spacing:0.5px;color:${C.MUTED};margin-bottom:7px;">Justification Statement</div>
      <p style="font-size:13px;color:${C.MID};line-height:1.8;margin:0;">${execText}</p>
    </div>

    <div style="background:${C.LIGHT_BG};border:1px solid ${C.BORDER};border-radius:8px;padding:14px 18px;margin-bottom:16px;">
      <div style="font-size:11px;font-weight:700;text-transform:uppercase;color:${C.MUTED};margin-bottom:10px;">Scoring Methodology Applied</div>
      <div style="display:grid;grid-template-columns:repeat(4,1fr);gap:12px;">
        ${[['Price', weights.price], ['Coverage', weights.coverage], ['Compliance', weights.compliance], ['Risk', weights.risk]].map(([l, w]) =>
          `<div style="text-align:center;background:white;border:1px solid ${C.BORDER};border-radius:6px;padding:12px 8px;">
            <div style="font-size:20px;font-weight:800;color:${C.ORANGE};">${w}%</div>
            <div style="font-size:10px;font-weight:700;text-transform:uppercase;color:${C.MUTED};margin-top:3px;">${l}</div>
          </div>`).join('')}
      </div>
    </div>

    <div style="border:1px solid ${C.BORDER};border-radius:10px;overflow:hidden;margin-bottom:18px;">
      <table>
        <thead style="background:${C.DARK};">
          <tr>
            <th style="padding:10px 12px;text-align:left;font-size:10px;font-weight:700;color:white;text-transform:uppercase;letter-spacing:0.5px;">Tenderer</th>
            <th style="padding:10px 12px;text-align:right;font-size:10px;font-weight:700;color:white;text-transform:uppercase;letter-spacing:0.5px;">Quoted Total</th>
            <th style="padding:10px 12px;text-align:center;font-size:10px;font-weight:700;color:white;text-transform:uppercase;letter-spacing:0.5px;">Coverage</th>
            <th style="padding:10px 12px;text-align:center;font-size:10px;font-weight:700;color:white;text-transform:uppercase;letter-spacing:0.5px;">Risk</th>
            <th style="padding:10px 12px;text-align:center;font-size:10px;font-weight:700;color:white;text-transform:uppercase;letter-spacing:0.5px;">Composite Score</th>
            <th style="padding:10px 12px;text-align:center;font-size:10px;font-weight:700;color:white;text-transform:uppercase;letter-spacing:0.5px;">Outcome</th>
          </tr>
        </thead>
        <tbody>${scoreRows}</tbody>
      </table>
    </div>

    ${drivers.length > 0 ? `<div style="border:1px solid ${C.BORDER};border-radius:8px;padding:16px 18px;">
      <div style="font-size:11px;font-weight:700;text-transform:uppercase;color:${C.MUTED};margin-bottom:10px;">Documented Decision Factors</div>
      ${drivers.map((d, i) => `<div style="display:flex;gap:10px;margin-bottom:7px;font-size:12px;color:${C.MID};">
        <span style="font-size:11px;font-weight:700;color:${C.MUTED};flex-shrink:0;">${i + 1}.</span>
        <span>${d}</span>
      </div>`).join('')}
    </div>` : ''}

    ${sharedFooter('Recommendation Justification', '4', true)}
  </div>`;
}

