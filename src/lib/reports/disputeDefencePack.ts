export interface DisputeDefencePackOptions extends ReportOptions {
  snapshotTimestamp?: string;
}

export function generateDisputeDefencePack(opts: DisputeDefencePackOptions): string {
  const snapshotTs = opts.snapshotTimestamp ?? new Date().toISOString();

  const body = [
    page1DecisionRecord(opts, snapshotTs),
    page2DataSnapshot(opts, snapshotTs),
    page3RiskIdentification(opts, snapshotTs),
    page4RecommendationJustification(opts, snapshotTs),
    page5CommercialWarnings(opts, snapshotTs),
    page6ApprovalTrace(opts, snapshotTs),
    page7DefenceStatement(opts, snapshotTs),
  ].join('\n');

  return htmlWrap(
    `Dispute Defence Pack — ${opts.projectName} — ${snapshotTs}`,
    baseStyles('screen'),
    body,
  );
}

export function openDisputeDefencePack(opts: DisputeDefencePackOptions, filename: string): void {
  openPrintWindow(generateDisputeDefencePack(opts), filename);
}
