/**
 * Dispute Defence Pack Generator
 *
 * Purpose: Generate an immutable, timestamped, formally worded document
 * suitable for use in procurement disputes, contract investigations,
 * audit proceedings, or regulatory review.
 *
 * Sections:
 *   1. Decision Record — Who decided, when, what was recommended
 *   2. Data Snapshot — All tenderer inputs and normalised outputs at time of decision
 *   3. Risk Identification — Pre-award risks that were known and documented
 *   4. Recommendation Justification — Scored rationale with weights
 *   5. Commercial Warnings Issued — Any warnings raised before award
 *   6. Approval Trace — Full audit chain (who approved, when, any overrides)
 *   7. Defence Statement — Formal declaration of process integrity
 *
 * IMPORTANT: This file contains NO calculation logic.
 * All values are passed in pre-computed from upstream engines and frozen at export time.
 */

import type { ReportOptions } from './reportTypes';
import {
  C, fmt, pct, riskRatio, riskLabel,
  sharedHeader, sharedFooter, sectionHeading, infoBox,
  baseStyles, htmlWrap, openPrintWindow,
} from './reportShared';

const PACK_SUBTITLE = 'Dispute Defence Pack — Procurement Record';

function packHeader(opts: ReportOptions, snapshotTs: string): string {
  return sharedHeader(opts.organisationLogoUrl, opts.projectName, opts.clientName, snapshotTs, PACK_SUBTITLE);
}

